def build_dataset(output_path: str | Path) -> None:
    """Write QA_PAIRS to JSONL, duplicating to reach 500+ examples."""
    output_path = Path(output_path)
    output_path.parent.mkdir(parents=True, exist_ok=True)

    # Augment by paraphrasing question prefixes to reach 500+
    augmented = list(QA_PAIRS)
    prefixes = ["ଦୟାକରି କୁହ,", "ମୋତେ ଜଣାଅ,", "ଆପଣ ଜାଣନ୍ତି କି,", "ସଂକ୍ଷେପରେ,",
                "ଏ ବିଷୟରେ,", "ଆଗ୍ରହ ଅଛି,"]
    for q, a in QA_PAIRS:
        for prefix in prefixes:
            augmented.append((f"{prefix} {q}", a))

    with open(output_path, "w", encoding="utf-8") as f:
        for q, a in augmented:
            record = {"question": q, "answer": a}
            f.write(json.dumps(record, ensure_ascii=False) + "\n")

    print(f"SFT dataset: {len(augmented)} examples → {output_path}")


